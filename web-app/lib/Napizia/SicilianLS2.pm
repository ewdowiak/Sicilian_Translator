##  DETOKENIZATION
##  ==============

sub sc_detokenizer {

    my $line = $_[0];
    $line = '<BOS> ' . $line . ' <EOS>';

    ##  reinsert spaces before and after punctuation
    #$line =~ s/([\,\.\?\!\:\;\%\}\]\)])/ $1 /g;

    ##  remove excess space
    $line =~ s/\s+/ /g;
    $line =~ s/^ //;
    $line =~ s/ $//;
    
    # ##  capitalize, accent and fix punctuation
    my $newline = sc_capitalize( $line );
    $newline = fix_punctuation( $newline );
    
    ##  remaining punctuation
    # $newline =~ s/' /'/g;
    # $newline =~ s/ '/'/g;
    $newline =~ s/c' /c'/g;
    $newline =~ s/C' /C'/g;
    $newline =~ s/d' /d'/g;
    $newline =~ s/D' /D'/g;
    $newline =~ s/l' /l'/g;
    $newline =~ s/L' /L'/g;
    $newline =~ s/m' /m'/g;
    $newline =~ s/M' /M'/g;
    $newline =~ s/n' /n'/g;
    $newline =~ s/N' /N'/g;
    $newline =~ s/p' /p'/g;
    $newline =~ s/P' /P'/g;
    $newline =~ s/s' /s'/g;
    $newline =~ s/S' /S'/g;
    $newline =~ s/t' /t'/g;
    $newline =~ s/T' /T'/g;
    $newline =~ s/v' /v'/g;
    $newline =~ s/V' /V'/g;
    
    ##  remove excess space
    $newline =~ s/\s+/ /g;
    $newline =~ s/^ //;
    $newline =~ s/ $//;

    ##  return the detokenized line
    return $newline;
}

