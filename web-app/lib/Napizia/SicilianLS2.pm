##  Sicilian tokenizer
sub sc_tokenizer {

    my $line = $_[0];

    ##  make it all lower case
    $line = lc( $line );
    $line =~ s/È/è/g;
    $line =~ s/É/è/g;
    $line =~ s/Ì/ì/g;
    $line =~ s/Í/ì/g;

    ##  replace acute accents with grave accents
    $line = swap_accents( $line );
    
    ##  separate punctuation, except apostrophe
    $line =~ s/([\-"\.,:;\!\?\(\)])/ $1 /g;
    
    ##  remove excess space
    $line =~ s/\s+/ /g;
    $line =~ s/^ //;
    $line =~ s/ $//;

    ##  add single space to beginning and end of line
    $line = " " . $line . " ";

    ##  form contractions, if necessary
    $line =~ s/ cu un / c'un /g;
    $line =~ s/ di un / d'un /g;

    ##  remove unnecessary apostrophes and accents
    $line =~ s/ po' / po /g;
    $line =~ s/ vo' / vo /g;
    $line =~ s/ me' / me /g;
    $line =~ s/ to' / to /g;
    $line =~ s/ so' / so /g;
    $line =~ s/ ne' / ne /g;
    
    $line =~ s/ pò / po /g;
    $line =~ s/ vò / vo /g;
    $line =~ s/ mè / me /g;
    $line =~ s/ tò / to /g;
    $line =~ s/ sò / so /g;
    $line =~ s/ nè / ne /g;


    ##  convert:  c'e' -- n'e' -- e' -- si'
    $line =~ s/ c'e' / c'e~ /g;
    $line =~ s/ c'è / c'e~ /g;
    $line =~ s/ n'e' / n'e~ /g;
    $line =~ s/ n'è / n'e~ /g;
    $line =~ s/ e' / e~ /g;
    $line =~ s/ è / e~ /g;
    $line =~ s/ si' / si~ /g;
    $line =~ s/ sì / si~ /g;

    ##  replace "<<" and ">>" with single quote
    $line =~ s/«/ " /g;
    $line =~ s/»/ " /g;
    
    ##  make sure there are no double double quotes
    $line =~ s/"\s+"/ " /g;
    
    ##  remove excess space
    $line =~ s/\s+/ /g;
    
    ##  contractions of conjunctive pronouns
    $line =~ s/ mû / mi lu /g;
    $line =~ s/ tû / ti lu /g;
    $line =~ s/ ciû / ci lu /g;
    $line =~ s/ cciû / ci lu /g;
    $line =~ s/ sû / si lu /g;
    $line =~ s/ nû / ni lu /g;
    $line =~ s/ vû / vi lu /g;

    $line =~ s/ m'û / mi lu /g;
    $line =~ s/ t'û / ti lu /g;
    $line =~ s/ ci'û / ci lu /g;
    $line =~ s/ cci'û / ci lu /g;
    $line =~ s/ s'û / si lu /g;
    $line =~ s/ n'û / ni lu /g;
    $line =~ s/ v'û / vi lu /g;

    $line =~ s/ mâ / mi la /g;
    $line =~ s/ tâ / ti la /g;
    $line =~ s/ ciâ / ci la /g;
    $line =~ s/ cciâ / ci la /g;
    $line =~ s/ sâ / si la /g;
    $line =~ s/ nâ / ni la /g;
    $line =~ s/ vâ / vi la /g;

    $line =~ s/ m'â / mi la /g;
    $line =~ s/ t'â / ti la /g;
    $line =~ s/ ci'â / ci la /g;
    $line =~ s/ cci'â / ci la /g;
    $line =~ s/ s'â / si la /g;
    $line =~ s/ n'â / ni la /g;
    $line =~ s/ v'â / vi la /g;

    $line =~ s/ mî / mi li /g;
    $line =~ s/ tî / ti li /g;
    $line =~ s/ cî / ci li /g;
    $line =~ s/ ccî / ci li /g;
    $line =~ s/ sî / si li /g;
    $line =~ s/ nî / ni li /g;
    $line =~ s/ vî / vi li /g;

    $line =~ s/ m'î / mi li /g;
    $line =~ s/ t'î / ti li /g;
    $line =~ s/ c'î / ci li /g;
    $line =~ s/ cc'î / ci li /g;
    $line =~ s/ s'î / si li /g;
    $line =~ s/ n'î / ni li /g;
    $line =~ s/ v'î / vi li /g;

    ##  remove excess space, again
    $line =~ s/\s+/ /g;
    $line =~ s/^ //;
    $line =~ s/ $//;
    
    ##  remove accents from each word
    ##  except ("è" = "is") and ("sì" = "you are")
    ##  and uncontract circumflex accents words
    my @words = split( / / , $line );
    my @no_accents ; 
    for my $i (0..$#words) {

	##  which word
	my $word = $words[$i];
	my $next_word = ( $i != $#words ) ? $words[$i+1] : "";
	
	my $new_word = $word;
	
	##  remove accents
	$new_word = finish_tilde( $word ) ;
	$new_word = rid_accents( $new_word );
	$new_word = lc( $new_word );
	
	##  replace abbreviated article with full form
	$new_word = ( $new_word eq "'u" ) ? "lu" : $new_word;
	$new_word = ( $new_word eq "'a" ) ? "la" : $new_word;
	$new_word = ( $new_word eq "'i" ) ? "li" : $new_word;
	
	##  more uncontractions
	$new_word = ( $new_word eq "'n" ) ? "in" : $new_word;
	$new_word = ( $new_word eq "n" ) ? "in" : $new_word;

	##  fix Eryk's mistakes
	$new_word = ( $new_word eq "grazzi"  ) ? "grazzii" : $new_word;
	$new_word = ( $new_word eq "seri"  ) ? "serii" : $new_word;
	$new_word = ( $new_word eq "stori"  ) ? "storii" : $new_word;
	
	##  replacements
	$new_word = ( $new_word eq "cca~"  ) ? "cca" : $new_word;
	$new_word = ( $new_word eq "chiu~"  ) ? "chiu" : $new_word;
	$new_word = ( $new_word eq "cchiu~" ) ? "chiu" : $new_word;
	$new_word = ( $new_word eq "cchiu"  ) ? "chiu" : $new_word;
	$new_word = ( $new_word eq "cci" ) ? "ci" : $new_word;
	$new_word = ( $new_word eq "dopu" ) ? "doppu" : $new_word;
	$new_word = ( $new_word eq "libru" ) ? "libbru" : $new_word;

	$new_word = ( $new_word eq "ngrisi" ) ? "nglisi" : $new_word;
	$new_word = ( $new_word eq "ingrisi" ) ? "inglisi" : $new_word;

	$new_word = ( $new_word eq "ne~" ) ? "ne" : $new_word;
	$new_word = ( $new_word eq "non" ) ? "nun" : $new_word;

	$new_word = ( $new_word eq "quacchi" ) ? "qualchi" : $new_word;
	$new_word = ( $new_word eq "quarchi" ) ? "qualchi" : $new_word;
	$new_word = ( $new_word eq "quarcunu" ) ? "qualcunu" : $new_word;
	
	$new_word = ( $new_word eq "peggiu" ) ? "peju" : $new_word;
	$new_word = ( $new_word eq "pir" ) ? "pi" : $new_word;
	$new_word = ( $new_word eq "pri" ) ? "pi" : $new_word;
	$new_word = ( $new_word eq "picchi~" ) ? "picchi" : $new_word;  ## Ô pruf. Cipolla piaci "pirchì."
	$new_word = ( $new_word eq "pirchi"  ) ? "picchi" : $new_word;  ## we'll fix that in detokenization
	$new_word = ( $new_word eq "pirchi~" ) ? "picchi" : $new_word;
	$new_word = ( $new_word eq "pircio~" ) ? "pircio" : $new_word;
	$new_word = ( $new_word eq "percio~" ) ? "pircio" : $new_word;
	$new_word = ( $new_word eq "soccu" ) ? "zoccu" : $new_word;
	$new_word = ( $new_word eq "sunu" ) ? "sunnu" : $new_word;
	    
	##  uncontract circumflex accented words
	$new_word = uncontract( $new_word , $next_word );
	
	##  push it out
	push( @no_accents , $new_word );
	
    }

    ##  join it all together
    my $newline = join( ' ' , @no_accents );

    ##  remove any remaining accents
    $newline = rid_accents( $newline );
    $newline = rid_circum(  $newline );
    $newline = lc( $newline );
    
    ##  insert space after an apostrophe
    $newline =~ s/'/' /g;
    
    ##  make it all lower case (again)
    $newline = lc( $newline );
    
    ##  remove excess space
    $newline =~ s/\s+/ /g;
    $newline =~ s/^ //;
